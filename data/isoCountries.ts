export const NAME_TO_ISO2: Map<string, string> = new Map(NAME_TO_ISO2_RAW);

// Extract visited country ISO-2 codes from a trip destination + stops
export function extractCountriesFromTrip(destination: string | null, stops: string[]): string[] {
  const found = new Set<string>();
  const sources = [destination, ...stops].filter(Boolean) as string[];
  for (const src of sources) {
    const lower = src.toLowerCase().trim();
    // Try exact match first
    const exact = NAME_TO_ISO2.get(lower);
    if (exact) { found.add(exact); continue; }
    // Try partial match — check if any known name/alias is contained in the string
    for (const [key, iso2] of NAME_TO_ISO2) {
      if (key.length >= 4 && lower.includes(key)) {
        found.add(iso2);
        break;
      }
    }
  }
  return Array.from(found);
}
